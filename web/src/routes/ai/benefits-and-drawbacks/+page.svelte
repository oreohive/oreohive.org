<div class="centre-container">
  <h1>The potential benefits</h1>
  <p>
    It'd be unfair of me to avoid shedding light on the legitimate positives of
    AI in how they can help assist human creation.
  </p>
  <ul>
    <li>
      <h3>Automation of sanitisation</h3>
      AI has the ability to automate repetetive tasks that may not be seen as important
      or expressive to a human. For example, reformatting a huge Python script that
      needs info to be formatted in a certain way can be automated using AI.
      <br /><br />There is an argument that the automation of such processes
      could take away from the feelings that effort truly went into the code,
      program, script or software.
      <br /><br />However, it's likely that such processes would be automated
      with some kind of script, and it could be argued that this leaves more
      time for means of human expression that some may prefer to partake in
      (like designing UI or writing content).
    </li>
    <li>
      <h3>AI isn't always generative for the sake of being generative</h3>
      AI isn't just generative.<br /><br />The current industry AI phenomenon,
      while in many ways arguably ridiculous, has pushed the use of AI in tools
      that may arguably be generative in nature, but serve to legitimately aid
      and help people to level a playing field, rather than to replace or
      override human means of creation.
      <br /><br />For example, Mozilla's Firefox browser recently used AI in a
      tool that automatically generates descriptions of images without
      accessibility alt text. This helps the blind and visually impaired
      interface with the media-driven world of the internet in ways they may
      have been barred from before.
    </li>
    <li>
      <h3>AI responses are manufactured but still tailored</h3>
      AI, while it gives the illusion of interactivity and consciousness, does deliver
      in personalising its responses to circumstances, and attuning itself to one's
      own circumstances or situation.<br /><br />
      It does what forum posts don't in applying directly to any given situation;
      even if it may fail to fill in the gaps properly, it does try, and sometimes,
      it can indeed be right as to what, say, package you need to install on a Linux
      system to accomplish a certain task.
      <br /><br />I would personally argue that a real human being would be able
      to do a much better job at this, but in times and circumstances where it's
      felt like a quicker response is needed than could be accomplished by
      posting on a forum, some may feel more allured to hit up an LLM to have it
      investigate a problem instead.
      <br /><br />This could especially be the case if an individual feels it
      would be burdensome, shameful or even pathetic to ask other human beings
      for help; in some such cases, it might be felt that AI's emotionlessness
      could be a double-sided coin.
    </li>
  </ul>
  <p>
    While <b
      >the solution for some may just be to get more friends interested in
      similar things</b
    >, these ideas could entice one to consider using these AI agents to help
    them in solving problems and aiding them in their own creations.
    <br /><br /><b>Especially in the cases of accessibility</b>, I do feel that
    generative AI does have the potential to properly transform certain pieces
    of information or media into formats more tailored towards certain
    individuals, including <b>ones with certain impairments</b>, disabilities or
    conditions (since these may make viewing the content in its initial form
    unviable or infeasible).
  </p>
</div>

<div class="centre-container">
  <h1>The very real issues</h1>
  <p>It's unfortunately not all sunshine and rainbows, though.</p>
  <p>
    My concerns with AI stem largely from ideas I hear that it can replace or
    replicate human creativity.
    <br /><br />In a world where
    <a href="https://youtu.be/SHKk5CxAXhg"
      >we are all being molded into one unified paste by social media</a
    >, encouraged to blend ourselves into trends for a capitalist broad reach,
    is more generalised, computational, algorithmic 'creativity' (that isn't
    actually so) what we need?
  </p>
  <ul>
    <li>
      <h3>The misunderstanding of art</h3>
      <p><b>Art is expression</b>.</p>
      <p>
        <br /><b>AI images are not art</b>. I'm sorry, it's just not really up
        for debate.
      </p>
      <p>
        <br />An AI-generated image or piece of text is an
        algorithmically-generated product of, well, an algorithm. It's at best
        just a way of displaying the original prompt, but even this isn't true
        given the randomness involved in how these AI agents are designed to
        work.
      </p>
      <p>
        AI image generation is, by my definitions, soulless. It's like if you
        asked someone to make you a cake, and it made you the most generic,
        average cake in the world, but one that was a fusion of about quite
        literally ten thousand different cakes.<br /><br />
      </p>
      <p>
        It's like a <a href="https://youtu.be/Nyh9V7N3E3w"
          ><b>banana and mayonnaise sandwich</b></a
        >; <b>meaningless</b>.<br /><br />
      </p>
      <p>
        It is indeed in the form factor of a sandwich, but it stands for nothing
        and, ultimately, means nothing.<br />If anything, it just means that
        someone felt too lazy to properly establish a perfect blend, a decent
        palette or any decent set of ingredients of any cohesion whatsoever.
      </p>
    </li>
    <li>
      <h3>AI gets things wrong</h3>
      <p>It just does. <b>Constantly</b>.<br /><br /></p>
      <p>
        I will admit that the issues with factual accuracy have been curbed
        somewhat, at least in my experimentations, with the 'reasoning' models
        like o1 that seem to do a better job of mimicking actual rationale and
        thought processes.
      </p>
      <p>
        However, when I come across an article on the internet, I want it to be
        written by a human. I don't want the dead internet theory to become an
        undisputable reality. I don't want to live in a doubtful world in which
        I am always faced with the possibility that what I'm looking at was spat
        out by an AI model rather than an actual human.<br /><br />
      </p>
      <p>
        We live in a world where <b>AI lacks proper regulations</b>, such that
        AI-generated content <b>lacks any kind of disclaimer</b> stating this
        kind of information.<br />As such, it is, at least as of right now,
        virtually impossible and impractical to filter all results when
        searching with the likes of Google Search such that they only feature
        authentic results from real humans, not capitalism-weaponised AI LLMs.
      </p>
    </li>
    <li>
      <h3>I want communities of real people</h3>
      <p>
        As I touched on, I don't want the '<a
          href="https://en.wikipedia.org/wiki/Dead_Internet_theory"
          >dead internet theory</a
        >' to become a cripplingly sad, sterile reality.
      </p>
      <p>
        With <b>so much manufactured content as it stands</b> without even
        involving generative AI, as the trend-establishing algorithms of today's
        social media age unify us in pursuit, I do not feel like we need to be
        <b>further liquified and blended into this one single pool</b> of slop and
        unified interest.
      </p>
      <p>
        These algorithms are already purging us of our individualities, quirks,
        hobbies and personalities. Why must we purge ourselves of this further?
      </p>
      <p>
        If we restrict our means of 'creation' to these machines, this will only
        exacerbate the problem.
      </p>
    </li>
  </ul>
  <p>
    Capitalism has dictated that <b
      >we replace many humans in jobs with machines</b
    >. I think that's just a cog in the capitalist wheel; it is what it is, it's
    cheaper and more efficient in the eyes of this business, so of course. They
    don't need to feed them, they don't need to treat them with so much respect,
    they needn't respect any human lives of them. They just need to purchase
    them or rent them, then maintain them.<br /><br />
    By contrast, my worry with AI image generation being seen or proposed to replace
    art and real human artists is that <b>art is not objective</b>, at least in
    my opinion, and it shouldn't be seen, acknowledged or approached as such.
  </p>
  <p>
    If the goal of art isn't to express organic feelings and emotions, then what
    is it? Ah, yes â€” to make money?
  </p>
  <p><b>No</b>. That's not what I feel art should be for.</p>
  <p>
    And no, artists asking for payment for commissions is not an example of
    this. Artists, in the system we live in, need to live too. <b
      >They've spent their time becoming skilled in a craft</b
    >, and this is not invalidated by the comparison of art to some objective
    process.
  </p>
</div>

<div class="centre-container">
  <h2>To conclude</h2>
</div>
<div class="centre-container">
  <p>Am I anti-AI? No, not necessarily.</p>
  <p>
    I feel that my stance on AI right now is very similar to the stance I've had
    on social media for such a long time.
  </p>
  <p>
    Once again, I'm left feeling like these technologies are not inherently
    evil, but are instead being used for the wrong things, whether that's in a
    chase for capitalism or just straight-up malice and hate.
  </p>
  <h3>So, here's where I stand.</h3>
  <p>
    AI and these kinds of machine learning / pattern recognition technologies do
    have the power to make real, positive differences in our world for a public
    good. A real public good.
  </p>
  <p>
    Means of artificial intelligence need not be used, in my eyes, for stealing
    art, for polluting the web and for giving people opportunities to disrespect
    those skilled at what they do.<br />
    Instead, we should be using this compute to conduct protein synthesis to cure
    cancers, provide accessibility tools for the web, and have them act merely as
    an aid for creativity, just like autocorrect and predictive text have always
    been for us.
  </p>
  <p>
    We should not undervalue the power of human creativity. Don't forget just
    what you are capable of. Expression can never be so properly mimicked and
    emulated; it is arguably either present, or it is not.
  </p>
  <p>
    So, <b>am I anti-AI</b>? Not really, no. However, I stand with the 'anti-AI'
    movements in protection of artists and real human art, because I feel that
    this is really what is being stood for.
  </p>
  <p>
    I feel <b>it is so dangerous</b> that AI is as <b>unregulated</b> as it is,
    and continues to be. We <b>must</b> be
    <b>shown with immense transparency</b>
    what is a human work and what is not, for the purposes of ensuring
    <b>factual accuracy</b>, logical thinking, rationale and reasoning, and
    ensuring that people do not let AI overstep in filling the internet with
    slop articles that effectively mean nothing.<br /><br />
    I feel <b>we've a right to know and understand</b> whether what we're being shown
    (well, spoonfed by these algorithms) is art as expressed by a real organism,
    and what is not.
  </p>
  <p>
    <i
      >Platforms have pockets full of individuals engaging with AI images, under
      the misguidance or influence that these are pieces of real art, or,
      alarmingly, real photographic images. Not too long ago on Facebook, I saw
      a picture of numerous cleanly-cut skeletons beneath the sea in a plane
      following a real-world plane crash. The claim was that this had been found
      in the real world; I may be savvy enough to have identified this as an
      AI-generated image rather than a real picture of our real world, but it
      appeared the same was not true for many, many people. Like, a lot of
      people.</i
    >
  </p>
  <p>
    The now widespread accessibility and lack of regulation on these kinds of
    generative AI tools has made this kind of misuse startlingly easy.
  </p>
  <p>
    <i>Let's not bury human creativity and real pictures of our real world.</i>
  </p>
